// Helper functions
export const getPresetsBySection = (section: string): PromptPreset[] => {
  return allPresets.filter((preset) => preset.section === section);
};

export const getPresetsByCategory = (
  category: PromptCategory,
): PromptPreset[] => {
  return allPresets.filter((preset) => preset.category === category);
};

export const getPresetsBySectionAndCategory = (
  section: string,
  category: PromptCategory,
): PromptPreset[] => {
  return allPresets.filter(
    (preset) => preset.section === section && preset.category === category,
  );
};

export const searchPresets = (
  query: string,
  presets: PromptPreset[] = allPresets,
): PromptPreset[] => {
  const lowerQuery = query.toLowerCase();

  return presets.filter(
    (preset) =>
      preset.name.toLowerCase().includes(lowerQuery) ||
      preset.content.toLowerCase().includes(lowerQuery) ||
      preset.tags.some((tag) => tag.toLowerCase().includes(lowerQuery)),
  );
};
